export function getAlaskaPath(): string {
  const points = ALASKA_COORDS.map(([lng, lat]) => {
    const x = 30 + ((lng + 180) / 50) * 120;
    const y = 480 + ((72 - lat) / 25) * 80;
    return `${x.toFixed(1)},${y.toFixed(1)}`;
  });
  return `M${points.join('L')}Z`;
}

export function getHawaiiPaths(): string[] {
  const project = (lng: number, lat: number) => {
    const x = 220 + ((lng + 161) / 8) * 80;
    const y = 510 + ((23 - lat) / 5) * 60;
    return `${x.toFixed(1)},${y.toFixed(1)}`;
  };
  return [
    `M${HAWAII_COORDS.map(([lng, lat]) => project(lng, lat)).join('L')}Z`,
    `M${HAWAII2_COORDS.map(([lng, lat]) => project(lng, lat)).join('L')}Z`,
  ];
}
