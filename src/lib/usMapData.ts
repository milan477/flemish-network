export const SVG_WIDTH = 960;
export const SVG_HEIGHT = 600;

const LNG_MIN = -125;
const LNG_MAX = -67;
const LAT_MIN = 24;
const LAT_MAX = 50;

export function projectToSvg(lat: number, lng: number): { x: number; y: number } {
  const x = ((lng - LNG_MIN) / (LNG_MAX - LNG_MIN)) * SVG_WIDTH;
  const y = ((LAT_MAX - lat) / (LAT_MAX - LAT_MIN)) * SVG_HEIGHT;
  return { x, y };
}

interface StatePolygon {
  abbr: string;
  coords: [number, number][];
}

function polyToPath(coords: [number, number][]): string {
  const points = coords.map(([lng, lat]) => {
    const { x, y } = projectToSvg(lat, lng);
    return `${x.toFixed(1)},${y.toFixed(1)}`;
  });
  return `M${points.join('L')}Z`;
}

